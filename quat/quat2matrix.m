function [ R ] = quat2matrix( q )

% assume norm(q) = 1

R11 = 1 - 2 * q(3)*q(3) - 2 *q(4)*q(4);
R12 = 2*q(2)*q(3) - 2*q(4)*q(1);
R13 = 2*q(2)*q(4) + 2*q(3)*q(1);

R21 = 2*q(2)*q(3) + 2*q(4)*q(1);
R22 = 1 - 2*q(2)*q(2) - 2*q(4)*q(4);
R23 = 2*q(3)*q(4) - 2*q(2)*q(1);

R31 = 2*q(2)*q(4) - 2*q(3)*q(1);
R32 = 2*q(3)*q(4) + 2*q(2)*q(1);
R33 = 1 - 2*q(2)*q(2) - 2*q(3)*q(3);

R = [ R11 R12 R13; R21 R22 R23; R31 R32 R33];
end